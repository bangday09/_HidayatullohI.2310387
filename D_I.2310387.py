sisi_kubus = float(input("\nMasukkan sisi kubus: "))

keliling_kubus = 12 * sisi_kubus
luas_kubus = 6 * (sisi_kubus ** 2)
volume_kubus = sisi_kubus ** 3

print(f"Keliling Kubus = \033[92m{keliling_kubus}\033[0m")
print(f"Luas Kubus = \033[92m{luas_kubus}\033[0m")
print(f"Volume Kubus = \033[92m{volume_kubus}\033[0m")